ORG 0H
LJMP MAIN
ORG 100H
MAIN:
CALL QUANT
HERE: SJMP HERE
ORG 130H
// *****************
QUANT:
MOV R2,#8
MOV R0,#60H
MOV R1,#70H

SKY:
MOV A,@R0
CLR C
SUBB A,#10
JC HIK

MOV A,@R0
CLR C
SUBB A,#20
JC SHZ

MOV A,@R0
CLR C
SUBB A,#30
JC ITZ

JMP V1

HIK:MOV @R1,#5
LJMP GO
SHZ:MOV @R1,#15
LJMP GO
ITZ:MOV @R1,#25
LJMP GO
V1:MOV @R1,#35
LJMP GO

GO:
INC R0
INC R1
DEC R2
CJNE R2,#0,SKY

RET
END